// Devuelve el idioma actual según la ruta (/en...) y el objeto de traducciones.
export function useT() {
  const { pathname } = useLocation()
  const lang = pathname === '/en' || pathname.startsWith('/en/') ? 'en' : 'es'
  return { lang, t: translations[lang] }
}

// Construye la ruta equivalente en el otro idioma (para el botón de idioma).
export function otherLangPath(pathname) {
  if (pathname === '/en' || pathname.startsWith('/en/')) {
    const rest = pathname.replace(/^\/en/, '')
    return rest === '' ? '/' : rest
  }
  return pathname === '/' ? '/en' : `/en${pathname}`
}
